// 2. Tính phần tử giữa của dãy số, rồi so sánh với số cần tìm. Nếu bằng thì return phần tử giữa đó, nếu target lớn 
// hơn thì lại gọi đệ quy hàm tìm kiếm với dãy số bên phải, nhỏ hơn thì bên trái.

// 3. Khi dãy đã sắp xếp mới sử dụng được Binary search. Trong trường hợp dãy đã được sắp xếp thì nên sử dụng binary 
// search

function binarySearch (arr, i, j, target) {
    const mid = Math.floor((i + j) / 2);
    if (arr[mid] === target) { 
        let count = 1;
        let a = mid - 1;
        let b = mid + 1;
        while (arr[a] === target) {
            count++;
            a--;
        }
        while (arr[b] === target) {
            count++;
            b++;
        }
        return count;
    }
    if (arr[mid] > target) return binarySearch(arr, i, mid - 1, target);
    return binarySearch(arr, mid+1, j, target);
}
